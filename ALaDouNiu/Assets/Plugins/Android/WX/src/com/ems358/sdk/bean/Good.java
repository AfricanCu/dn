package com.ems358.sdk.bean;

public class Good {

	private String uid;
	private int payid;
	private String goodName;
	private int goodPrice;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public int getPayid() {
		return payid;
	}

	public void setPayid(int payid) {
		this.payid = payid;
	}

	public String getGoodName() {
		return goodName;
	}

	public void setGoodName(String goodName) {
		this.goodName = goodName;
	}

	public int getGoodPrice() {
		return goodPrice;
	}

	public void setGoodPrice(int goodPrice) {
		this.goodPrice = goodPrice;
	}

	public Good() {
		super();
		// TODO Auto-generated constructor stub
	}

	public Good(String uid, int payid, String goodName, int goodPrice) {
		super();
		this.uid = uid;
		this.payid = payid;
		this.goodName = goodName;
		this.goodPrice = goodPrice;
	}

	public Good(String uid, String goodName, int goodPrice) {
		super();
		this.uid = uid;
		this.goodName = goodName;
		this.goodPrice = goodPrice;
	}

	public Good(int payid, String goodName, int goodPrice) {
		super();
		this.payid = payid;
		this.goodName = goodName;
		this.goodPrice = goodPrice;
	}

	public Good(String goodName, int goodPrice) {
		super();
		this.goodName = goodName;
		this.goodPrice = goodPrice;
	}

}
